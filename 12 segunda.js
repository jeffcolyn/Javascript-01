//  Faça um programa onde entramos com dois números, e ele imprime se o primeiro número é maior que o segundo.
//   Ex: 2 e 5. Imprime: false.

// recebe os números do usuário
const numero1 = 2
const numero2 = 5

// verifica se o primeiro número é maior que o segundo
if (numero1 > numero2) {
  console.log(`${numero1} é maior que ${numero2}. true.`);
} else {
  console.log(`${numero1} não é maior que ${numero2}. false.`);
}


